#include<stdio.h>
int main()
{
    for(int i=0;i<=4;i++){
        for(int j=0;j<=i;j++){
            printf("%d ",i);
        }
        printf("\n");              
    }
    for(int k=4;k<=0;k--){
        for(int l=0;l<=k;l--){
            printf("%d ",i);
        }
        printf("\n");              
    }
}